@router.delete("/{item_id}", status_code=204)
def delete_gallery_item(
    item_id: str,
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db)
):
    item = db.query(GalleryItem).filter(GalleryItem.id == item_id).first()
    if not item:
        raise HTTPException(status_code=404, detail="Item not found")
    
    project = db.query(Project).filter(
        Project.id == item.project_id, Project.owner_id == current_user.id
    ).first()
    if not project:
        raise HTTPException(status_code=403, detail="Access denied")
    
    if item.file_path and os.path.exists(item.file_path):
        os.remove(item.file_path)
    
    db.delete(item)
    db.commit()


# ─── Generators ───────────────────────────────────────────────────────────────
def _generate_pdf(title: str, content: str, out_dir: str, item_id: str):
    try:
        from reportlab.lib.pagesizes import letter
        from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
        from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer
        from reportlab.lib.units import inch

        path = os.path.join(out_dir, f"{item_id}.pdf")
        doc = SimpleDocTemplate(path, pagesize=letter)
        styles = getSampleStyleSheet()
        story = []

        # Title
        title_style = ParagraphStyle('Title', parent=styles['Heading1'], fontSize=24, spaceAfter=20)
        story.append(Paragraph(title, title_style))
        story.append(Spacer(1, 0.2 * inch))

        # Subtitle
        story.append(Paragraph("Generated by CollabMind AI", styles["Italic"]))
        story.append(Paragraph(datetime.now().strftime("%B %d, %Y"), styles["Normal"]))
        story.append(Spacer(1, 0.4 * inch))

        # Content
        if content:
            for para in content.split("\n"):
                if para.strip():
                    story.append(Paragraph(para.strip(), styles["Normal"]))
                    story.append(Spacer(1, 0.1 * inch))
        else:
            story.append(Paragraph(
                "This document was generated from your CollabMind AI knowledge base. "
                "Upload documents and use the Generate PDF button to create grounded content.",
                styles["Normal"]
            ))

        doc.build(story)
        return path, os.path.getsize(path)
    except ImportError:
        raise Exception("reportlab not available")


def _generate_pptx(title: str, content: str, out_dir: str, item_id: str):
    try:
        from pptx import Presentation
        from pptx.util import Inches, Pt

        path = os.path.join(out_dir, f"{item_id}.pptx")
        prs = Presentation()
        
        # Title slide
        slide_layout = prs.slide_layouts[0]
        slide = prs.slides.add_slide(slide_layout)
        slide.shapes.title.text = title
        slide.placeholders[1].text = "Generated by CollabMind AI"

        # Content slides
        lines = [l.strip() for l in (content or "Add content here").split("\n") if l.strip()]
        
        for i in range(0, max(len(lines), 3), 3):
            slide_layout = prs.slide_layouts[1]
            slide = prs.slides.add_slide(slide_layout)
            slide.shapes.title.text = f"Section {i // 3 + 1}"
            body = slide.placeholders[1]
            tf = body.text_frame
            tf.text = lines[i] if i < len(lines) else "Content"
            for j in range(1, 3):
                if i + j < len(lines):
                    p = tf.add_paragraph()
                    p.text = lines[i + j]

        prs.save(path)
        return path, os.path.getsize(path)
    except ImportError:
        raise Exception("python-pptx not available")


def _generate_notes(title: str, content: str, out_dir: str, item_id: str):
    # Notes are also PDFs with structured formatting
    notes_content = f"Study Notes: {title}\n\nKey Points:\n• " + "\n• ".join(
        [l.strip() for l in (content or "Summarized notes from your documents").split("\n") if l.strip()][:10]
    )
    return _generate_pdf(f"Notes: {title}", notes_content, out_dir, item_id)


