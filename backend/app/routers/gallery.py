router = APIRouter(prefix="/gallery", tags=["gallery"])
settings = get_settings()


@router.get("/{project_id}", response_model=List[GalleryItemOut])
def list_gallery(
    project_id: str,
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db)
):
    project = db.query(Project).filter(Project.id == project_id).first()
    if not project:
        raise HTTPException(status_code=404, detail="Project not found")
    
    items = db.query(GalleryItem).filter(GalleryItem.project_id == project_id)\
               .order_by(GalleryItem.created_at.desc()).all()
    return [GalleryItemOut.model_validate(i) for i in items]


@router.post("/generate", response_model=GalleryItemOut, status_code=201)
def generate_output(
    data: GenerateRequest,
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db)
):
    project = db.query(Project).filter(Project.id == data.project_id).first()
    if not project:
        raise HTTPException(status_code=404, detail="Project not found")

    gallery_dir = os.path.join(settings.upload_dir, data.project_id, "gallery")
    os.makedirs(gallery_dir, exist_ok=True)

    import uuid
    item_id = str(uuid.uuid4())
    file_size = 0
    file_path = None

    try:
        if data.output_type == "pdf":
            file_path, file_size = _generate_pdf(data.title, data.content or "", gallery_dir, item_id)
        elif data.output_type == "ppt":
            file_path, file_size = _generate_pptx(data.title, data.content or "", gallery_dir, item_id)
        elif data.output_type == "notes":
            file_path, file_size = _generate_notes(data.title, data.content or "", gallery_dir, item_id)
        elif data.output_type == "quiz":
            file_path, file_size = _generate_quiz(data.title, data.content or "", gallery_dir, item_id)
    except Exception as e:
        # Fallback: create a simple text file
        file_path = os.path.join(gallery_dir, f"{item_id}.txt")
        content_text = f"{data.title}\n\nGenerated by CollabMind AI\n\n{data.content or 'Content placeholder'}"
        with open(file_path, "w") as f:
            f.write(content_text)
        file_size = os.path.getsize(file_path)

    item = GalleryItem(
        id=item_id,
        project_id=data.project_id,
        output_type=data.output_type,
        title=data.title,
        file_path=file_path,
        file_size=file_size,
        item_metadata={"generated_by": "CollabMind AI", "version": "1.0"},
    )
    db.add(item)
    db.commit()
    db.refresh(item)
    
    return GalleryItemOut.model_validate(item)


@router.get("/download/{item_id}")
def download_gallery_item(
    item_id: str,
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db)
):
    item = db.query(GalleryItem).filter(GalleryItem.id == item_id).first()
    if not item:
        raise HTTPException(status_code=404, detail="Item not found")
    
    if not item.file_path or not os.path.exists(item.file_path):
        raise HTTPException(status_code=404, detail="File not found on storage")

    filename_map = {"pdf": ".pdf", "ppt": ".pptx", "notes": ".pdf", "quiz": ".pdf"}
    ext = filename_map.get(item.output_type, ".txt")
    mime_map = {
        "pdf": "application/pdf",
        "ppt": "application/vnd.openxmlformats-officedocument.presentationml.presentation",
        "notes": "application/pdf", "quiz": "application/pdf",
    }
    media_type = mime_map.get(item.output_type, "application/octet-stream")
    
    filename = f"{item.title.replace(' ', '_')}{ext}"
    return FileResponse(path=item.file_path, filename=filename, media_type=media_type)


